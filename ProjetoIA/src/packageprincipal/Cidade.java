package packageprincipal;

/**
 *
 * @author Antonio Junior
 */
public class Cidade {
    private String nome;
    private String pais;
    
    public Cidade(String name){
        this.nome = name;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getPais() {
        return pais;
    }

    public void setPais(String pais) {
        this.pais = pais;
    }
    
}
